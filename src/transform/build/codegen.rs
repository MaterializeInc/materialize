fn rule(r: &Rule) -> String {
    let conds: Vec<String> = r.conds.iter().map(cond).collect();
    format!(
        "{P}::Rule {{ name: {}, doc: {}, phase: {}, colored: {}, lhs: {}, rhs: {}, conds: vec![{}] }}",
        s(&r.name),
        opt_s(&r.doc),
        phase(&r.phase),
        r.colored,
        pat(&r.lhs),
        tmpl(&r.rhs),
        conds.join(", ")
    )
}
